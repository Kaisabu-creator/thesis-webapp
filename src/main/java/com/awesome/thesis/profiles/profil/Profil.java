package com.awesome.thesis.profiles.profil;

public class Profil {
    private long id;
    private String name;

    public Profil(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
